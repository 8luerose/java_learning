public class variable {
    public static void main(String[] args){
        int a = 1;                  //정수
        System.out.println(a);
        double b = 1.1;             //실수
        System.out.println(b);
        String c = "Hello World";   //문자열: 자바는 문자열 지원한다
        System.out.println(c);
    }
}
