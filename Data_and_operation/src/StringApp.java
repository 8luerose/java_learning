public class StringApp {
    public static void main(String[] args){
        System.out.println("Hello World!");     //String
        System.out.println('H');                //Char
        System.out.println("Hello " +           // + 는 인텔리제이 자체 기능
                "World");                       // Hello + World = 문장 더하기를 의미
        System.out.println("Hello \"Wolrd\"");  // \= 이스케이프 문자
        System.out.println("Hello\n Wolrd");    // \n = 줄바꿈

    }
}
