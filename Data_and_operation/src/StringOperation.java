public class StringOperation {
    public static void main(String[] args){
        System.out.println("Hello World".length());     //문자열.length() = 문자열 길이
        System.out.println("Hello, [name]".replace("[name]", "egoing"));
        //문자열.replace("{target}", "{replacement}")
        // =문자열.replace: target 을 replacement로 바꾸는 함수
    }
}
